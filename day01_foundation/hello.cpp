// 🧩 1. 第一个 C++ 程序

#include <iostream> // 引入输入输出流库

int main() {
    std::cout << "Hello, World!" << std::endl; // 输出一句话
    std::cout << "This is my first cpp program! " << std::endl;
    return 0; // 表示程序正常结束
}


/*
#include <iostream>：告诉编译器我们要使用标准输入输出功能。
int main()：主函数，是每个 C++ 程序的入口点。
std::cout：用于向控制台输出信息。
<<：插入运算符，把右边的内容输出到左边的对象中。
std::endl：换行符，并刷新输出缓冲区。
return 0;：表示程序成功执行完毕。
*/